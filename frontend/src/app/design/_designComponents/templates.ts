// Helper functions
export const getAllTemplates = (): WebTemplate[] => {
  return Object.values(WEB_TEMPLATES);
};

export const getTemplateById = (id: number): WebTemplate | undefined => {
  return WEB_TEMPLATES[id];
};

export const getTemplatesByCategory = (category: string): WebTemplate[] => {
  if (category === 'All') return getAllTemplates();
  return getAllTemplates().filter(template => template.category === category);
};